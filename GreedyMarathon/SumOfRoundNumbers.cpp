void solve(){

	ll n; cin >> n;
	vector<string> res;
	int zeroes = 0;
	while(n > 0){
		int num = n % 10;
		if(num != 0){
			string zero(zeroes, '0');
			res.push_back(to_string(num) + zero);
		}
		zeroes++;
		n /= 10;
	}
	cout << res.size() << ln;
	for(auto i: res)
	    cout << i << " ";

}
